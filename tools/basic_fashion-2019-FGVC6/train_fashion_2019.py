class ClothDataset(Dataset):
    """A dataset to prepare the instance and class images with the prompts for fine-tuning the model.

    Args:
        Dataset (_type_): _description_
    """
    def __init__(
        self,
        instance_data_root,
        instance_mode,
        instance_file,
        label_descriptions_file,
        tokenizer=None,
        instance_prompt=None,
        class_data_root=None,
        class_prompt=None,
        class_num=None,
        size=512,
        center_crop=False,
        random_flip=False,
    ):
        self.size = size
        self.center_crop = center_crop
        self.tokenizer = tokenizer
        self.val_num = 10
        self.instance_data_root = instance_data_root
        
        if not os.path.exists(self.instance_data_root):
            raise ValueError(f"Instance {self.instance_data_root} images root does not exists.")
        
        train_csv = os.path.join(instance_data_root, instance_file)
        label_file = os.path.join(instance_data_root, label_descriptions_file)
        self.df = pd.read_csv(train_csv)
        self.df["CategoryId"] = self.df["ClassId"].str.split('_').str[0]
        self.df["AttributeId"] = self.df["ClassId"].str.split('_').str[1:]
        with open(label_file) as f:
            label_descriptions = json.load(f)
        self.label_names = [x['name'] for x in label_descriptions["categories"]]
        
        all_img_ids = self.df["ImageId"].unique()
        self.instance_mode = instance_mode
        if self.instance_mode == "train":
            self.img_ids = all_img_ids[:-self.val_num]
        else:
            self.img_ids = all_img_ids[-self.val_num:]
            
        self.num_instance_images = len(self.img_ids)
        self._length = self.num_instance_images
        self.instance_prompt = instance_prompt
        self.use_random = True
        
        self.image_transforms = transforms.Compose(
            [
                transforms.Resize(size, interpolation=transforms.InterpolationMode.BILINEAR),
                transforms.CenterCrop(size) if center_crop else transforms.RandomCrop(size),
                transforms.RandomHorizontalFlip() if random_flip else transforms.Lambda(lambda x: x),
                transforms.ToTensor(),
                transforms.Normalize([0.5], [0.5])
            ]
        )
        self.val_transforms = transforms.Compose(
            [
                transforms.Resize(size, interpolation=transforms.InterpolationMode.BILINEAR),
                transforms.ToTensor(),
                transforms.Normalize([0.5], [0.5])
            ]
        )
        self.resize_op = transforms.Resize(size, interpolation=transforms.InterpolationMode.BILINEAR)
        
    def __len__(self):
        return self._length
    
    def get_img(self, img_id):
        IMG = cv2.imread(os.path.join(self.instance_data_root, "train", img_id), cv2.IMREAD_COLOR)
        IMG = cv2.cvtColor(IMG, cv2.COLOR_BGR2RGB)
        IMG = cv2.resize(IMG, (512, 512), interpolation=cv2.INTER_AREA)
        return IMG
    
    def get_mask(self, img_id):
        a = self.df[self.df.ImageId == img_id]
        a = a.groupby("CategoryId", as_index=False).agg({"EncodedPixels": ' '.join, 'Height': 'first', 'Width': 'first'})
        H = a.iloc[0, 2]
        W = a.iloc[0, 3]
        masks = []
        categories = []
        for line in a[['EncodedPixels', 'CategoryId']].iterrows():
            mask = np.full(H*W, dtype='int', fill_value=0)
            EncodePixels = line[1][0]
            pixel_loc = list(map(int, EncodePixels.split(' ')[0::2]))
            iter_num = list(map(int, EncodePixels.split(' ')[1::2]))
            for p, i in zip(pixel_loc, iter_num):
                mask[p : (p+i)] = line[1][1]
            mask = mask.reshape(W, H).T
            masks += [mask]
            categories += [line[1][1]]
        return masks, categories
    
    def new_mask(self, mask):
        matrix = np.zeros((512, 512), dtype='int')
        for i in range(0, len(mask)):
            mask[i] = cv2.resize(mask[i], (512, 512), interpolation=cv2.INTER_NEAREST)
        for m in mask:
            matrix += m
        return matrix
    
    def get_caption(self, categories):
        cat_ids = [int(x) for x in categories]
        cat_names = [self.label_names[x] for x in cat_ids]
        captions = ';'.join(cat_names)
        return captions
    
    def get_mask_maskImg(self, img_id):
        masked_list, categories = self.get_mask(img_id)
        IMG = self.get_img(img_id)
        if self.use_random:
            tmpinds = [x for x in range(len(masked_list))]
            tmplen = random.choice(tmpinds) + 1
            tmplen = max(tmplen, 2)
            random.shuffle(tmpinds)
            tmpinds = tmpinds[:tmplen]
            masked_list = [masked_list[x] for x in tmpinds]
            categories = [categories[x] for x in tmpinds]
            
        new_mask_ = self.new_mask(masked_list)
        
        mask = IMG.copy()
        mask[new_mask_ == 0] = 0
        maskIMG = IMG.copy()
        maskIMG[new_mask_ > 0] = 255
        maskCategories = self.get_caption(categories)
        return IMG, mask, maskIMG, maskCategories
    
    def tokenize_captions_dataset(self, examples):
        inputs = self.tokenizer(examples, max_length=self.tokenizer.model_max_length, padding="max_length", truncation=True, return_tensors="pt")
        return inputs.input_ids
    
    def __getitem__(self, index):
        example = {}
        img_id = self.img_ids[index]
        
        IMG, mask, maskImg, maskCategories = self.get_mask_maskImg(img_id)
        
        edit_image = Image.fromarray(IMG)
        if not edit_image.mode == "RGB":
            edit_image = edit_image.convert("RGB")
        instacne_image = Image.fromarray(maskImg)
        if not instacne_image.mode == "RGB":
            instacne_image = instacne_image.convert("RGB")
        instacne_mask = Image.fromarray(mask)
        if not instacne_image.mode == "RGB":
            instacne_mask = instacne_mask.convert("RGB")
            
        example["input_ids"] = self.tokenize_captions_dataset(maskCategories)
        
        if self.instance_mode == "train":
            # example["edit_images"] = self.image_transforms(edit_image)
            # example["instance_images"] = self.image_transforms(instacne_image)
            example["edit_images"] = self.val_transforms(edit_image)
            example["instance_images"] = self.val_transforms(instacne_image)
            example["instance_prompt_mask"] = self.image_transforms(instacne_mask)
        else:
            example["edit_images"] = self.val_transforms(edit_image)
            example["instance_images"] = self.val_transforms(instacne_image)
            example["instance_prompt_mask"] = self.val_transforms(instacne_mask)
            
            example["ori_edit_images"] = self.resize_op(edit_image)
            example["ori_instance_prompt_mask"] = self.resize_op(instacne_mask)
            example["ori_text_prompt_mask"] = maskCategories
            
        return example
    
def collate_fn(examples, mode="train", with_prior_preservation=False):
    edit_values = [example["edit_images"] for example in examples]
    pixel_values = [example["instance_images"] for example in examples]
    prompt_mask = [example["instance_prompt_mask"] for example in examples]
    
    edit_values = torch.stack(edit_values)
    edit_values = edit_values.to(memory_format=torch.contiguous_format).float()
    pixel_values = torch.stack(pixel_values)
    pixel_values = pixel_values.to(memory_format=torch.contiguous_format).float()
    prompt_mask = torch.stack(prompt_mask)
    prompt_mask = prompt_mask.to(memory_format=torch.contiguous_format).float()
    input_ids = torch.stack([example["input_ids"][0] for example in examples])
    
    batch = {
        "edit_values": edit_values,
        "pixel_values": pixel_values,
        "prompt_mask": prompt_mask,
        "input_ids": input_ids
    }
    if mode == 'val':
        ori_edit_values = [example["ori_edit_images"] for example in examples]
        ori_prompt_mask = [example["ori_instance_prompt_mask"] for example in examples]
        ori_text_prompt_mask = [example["ori_text_prompt_mask"] for example in examples]
        batch["ori_edit_values"] = ori_edit_values
        batch["ori_prompt_mask"] = ori_prompt_mask
        batch["ori_text_prompt_mask"] = ori_text_prompt_mask
    return batch
    
