def log_validation(text_encoder, tokenizer, unet, vae, args, accelerator, weight_dtype, epoch, val_dataloader, ema_unet):
    logger.info("Running validation... \n ")
    # create pipeline
    if args.use_ema:
        # Store the UNet parameters temporarily and load the EMA parameters to perform inference.
        ema_unet.store(unet.parameters())
        ema_unet.copy_to(unet.parameters())
    # The models need unwrapping because for compatibility in distributed training mode.
    pipeline = StableDiffusionClothPipeline.from_pretrained(
        args.pretrained_model_name_or_path,
        unet=accelerator.unwrap_model(unet),
        text_encoder=accelerator.unwrap_model(text_encoder),
        vae=accelerator.unwrap_model(vae),
        revision=args.revision,
        torch_dtype=weight_dtype,
    )
    pipeline = pipeline.to(accelerator.device)
    pipeline.set_progress_bar_config(disable=True)
    
    generator = None if args.seed is None else torch.Generator(device=accelerator.device).manual_seed(args.seed)

    image_logs = []
    for step, batch in enumerate(val_dataloader):
        original_image = batch["pixel_values"]
        prompt_mask = batch["prompt_mask"]
        target_values = batch["edit_values"]
        validation_prompt = batch["ori_text_prompt_mask"]

        images = []
        for _ in range(args.num_validation_images):
            with torch.autocast("cuda"):
                image = pipeline(
                    validation_prompt,
                    image=original_image,
                    prompt_mask=prompt_mask,
                    num_inference_steps=20,
                    image_guidance_scale=1.5,
                    guidance_scale=7,
                    generator=generator
                ).images[0]

            images.append(image)

        image_logs.append(
            {"validation_image": batch["ori_edit_values"][0], "prompt_mask": batch["ori_prompt_mask"][0], "images": images}
        )

    for tracker in accelerator.trackers:
        if tracker.name == "tensorboard":
            for i, log in enumerate(image_logs):
                images = log["images"]
                prompt_mask = log["prompt_mask"]
                validation_image = log["validation_image"]

                formatted_images = []

                formatted_images.append(np.asarray(validation_image))
                formatted_images.append(np.asarray(prompt_mask))

                for image in images:
                    formatted_images.append(np.asarray(image))

                formatted_images = np.stack(formatted_images)

                tracker.writer.add_images('_'.join(validation_prompt + [str(i)]), formatted_images, step, dataformats="NHWC")
        elif tracker.name == "wandb":
            formatted_images = []

            for log in image_logs:
                images = log["images"]
                validation_prompt = log["validation_prompt"]
                validation_image = log["validation_image"]

                formatted_images.append(wandb.Image(validation_image, caption="Controlnet conditioning"))

                for image in images:
                    image = wandb.Image(image, caption=validation_prompt)
                    formatted_images.append(image)

            tracker.log({"validation": formatted_images})
        else:
            logger.warn(f"image logging not implemented for {tracker.name}")
            
        if args.use_ema:
            # Switch back to the original UNet parameters.
            ema_unet.restore(unet.parameters())

        del pipeline
        torch.cuda.empty_cache()

        return image_logs


